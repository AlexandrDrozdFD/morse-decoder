const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    
    let char = expr.split('**********');
    let sign = '';
    
    for (const ch of char) {
        const letters = ch.match(/\d{10}/g).reduce((prevValue, item) => {
            prevValue += MORSE_TABLE[item.replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-')];
            return prevValue;
        }, '');
        sign += letters + ' ';
    }
    return sign.trim();


}

module.exports = {
    decode
}